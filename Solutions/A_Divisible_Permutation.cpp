template<typename T> void outV(const vector<T>& v) {if (v.empty()) return;
    for (int i = 0; i < sz(v); ++i)cout << v[i] << " \n"[i == sz(v) - 1];}

void run_case(){
    int n;    cin >> n;
    vi p;
    int mn = 1, mx = n;
    bool ok = 1;
    while (mn <= mx) {
        if (ok) p.push_back(mx--);
        else p.push_back(mn++);
        ok = !ok;
    }
    reverse(all(p));
    for (int i = 0; i < n; i++) {
        cout << p[i] << (i == n - 1 ? "\n" : " ");
    }
}

int32_t main(){
    ios_base::sync_with_stdio(0);
    cin.tie(0);          int T=1;
    if(!(cin >> T))     return 0;
    while(T--)        run_case();
    return 0;
}

