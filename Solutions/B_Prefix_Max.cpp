template<class T>    void inV(vector<T> &v){for(auto &x:v) cin>>x;}
template<typename T> void outV(const vector<T>& v) {if (v.empty()) return;
    for (int i = 0; i < sz(v); ++i)cout << v[i] << " \n"[i == sz(v) - 1];}

ll calculate_sum(const vector<ll>& a) {
    ll total_sum = 0;
    ll cur = 0;
    for (ll x : a) {
        cur = max(cur, x);
        total_sum += cur;
    }
    return total_sum;
}


void run_case(){
    ll n; cin>>n;
    vll a(n); inV(a);
    ll max_sum= calculate_sum(a);
    for(ll i=0; i<n; i++){
        for(int j=i+1; j<n; j++){
            swap(a[i], a[j]);
            max_sum = max(max_sum, calculate_sum(a));
            swap(a[i], a[j]);  
        }
    }
    cout<<max_sum<<nl;
}

int32_t main(){
    ios_base::sync_with_stdio(0);
    cin.tie(0);          int T=1;
    if(!(cin >> T))     return 0;
    while(T--)        run_case();
    return 0;
}

